#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int	i;
	int b;

	b = 0;
	i = 0;
	b = ft_strlen(s1);
	while (s2[i])
		s1[b++] = s2[i++];
	s1[b] = '\0';
	return (s1);
}
