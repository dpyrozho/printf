#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int	i;
	int b;

	b = 0;
	i = 0;
	if (n == 0)
		return (s1);
	b = ft_strlen(s1);
	while (s2[i] && n != 0)
	{
		s1[b++] = s2[i++];
		n--;
	}
	s1[b] = '\0';
	return (s1);
}
