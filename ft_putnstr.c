#include <unistd.h>

void	ft_putnstr(char const *s, int c)
{
	int	i;

	if (s != NULL)
	{
		i = 0;
		while ((i <= c) && s[i])
		{
			write(1, &s[i], 1);
			i++;
		}
	}
}
